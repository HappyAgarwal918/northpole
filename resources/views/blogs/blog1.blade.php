                                            <p>COVID-19, currently this word is creating panic among the people and the global pandemic is announced. People are afraid of going outside and touching each other. Currently, we are in a difficult moment due to this virus outbreak. So it’s become very important to know about the origin of this virus and what precautions you can take to protect yourself from this virus. So let’s get started.<br>

Coronaviruses were firstly founded in humans in year 1960s. Viruses can cause flu and gastrointestinal problems. The virus can also infect cattle, dogs, pigs, camels, and birds apart from humans.<br>

The virus is named corona because of its crown-like appearance under an electron microscope. The new coronavirus outbreak is linked to a market in Wuhan that sold meat and live animals. It is thought that it is originated in animals. The Wuhan coronavirus outbreak bears similar to the 2002-03 epidemic s of SARS (severe acute respiratory syndrome SARS-CoV). The SARS-CoV outbreak, which was started in South China, lasted for more than nine months. The first case of COVID-19 was detected in Wuhan, China on 31 Dec 2019. In India, the first positive case of new coronavirus was found in Kerela, who was a student from Wuhan, China.</p>
                                            <p> 
											In humans, coronaviruses are thought to cause a significant amount of all common colds. So there are seven strains of the coronaviruses documented till date:-<br>

>Human Coronavirus 229E (HCoV-229E)<br>
>Human Corona Virus OC43 (HCoV-OC)<br>
>New Heaven Corona Virus (HCoV-NL63)<br>
>Severe Acute Respiratory Syndrome (SARS-CoV)<br>
>Human Corona Virus HKU1<br>
>Middle East-_Respiratory Syndrome coronavirus (MERS-CoV)<br>
>Novel Coronavirus (2019-nCoV) or COVID-19<br>
And this COVID-19 is the reason for this global pandemic and the health crisis we are facing today. Should we worry about it? Is it really dangerous? Why is COVID-19 is a cause of concern?<br>

The COVID-19 has never been observed before, hence, there is no specific treatment for the disease. Current anti-viral treatments are also deemed ineffective at curing the illness. Therefore, present treatment is focused only on removing the symptoms of the disease. Initially, the COVID-19 was thought to be a zoonotic disease, however, current findings suggest that this disease spreads from person to person. It is found that it spreads through respiratory droplets (an infected person sneezing or coughing). The majority of people recover out of total worldwide confirmed cases. 40% of people are ill, 56.6% of people are recovered and 3.5% of people have died. The mortality of COVID-19 is 2%, which means if 100 are suffering from this disease, then chances are that only two people will die. And world health organization also revealed that the majority of people who died were above 60, have a weak immune system and have pre-existing medical conditions like cardiovascular disease, diabetes, and abnormally high blood pressure. The severity of infection is associated with age (60+) and underlying conditions.<br>

So how can you tell if someone is suffering from this disease? What are the symptoms? The reported symptoms of this viral infection include:-<br>

>Dry Cough<br>
>Shortness of Breath<br>
>Fever<br>
>Respiratory Distress<br>
>Runny Nose<br>
>Sore Throat<br>
Severe infection can lead to other life-threatening conditions such as:-<br>

>Pneumonia<br>
>Kidney Failure<br>
>Death usually occurs as a result of respiratory failure<br>

These are some symptoms you can notice in infected peoples. So what are the solutions for this outbreak? Is there any vaccine? The answer is no. Yes, there is no vaccine or antidote invented for this virus yet. So what we can do the most is take safety measures. These are:-<br>

>Regular hand wash<br>
>Wearing a mask when you are outside<br>
>Thoroughly cooking meat and eggs<br>
>Maintain distance from infected peoples<br>
>Avoid gathering at crowded places<br>
People are petrified due to this outbreak and it is obvious that fake rumors are spreading about COVID-19, which is misguiding the people and creating fear among them. Here are answers to most common myths:-<br>

Taking a hot bath cannot prevent the new coronavirus disease<br>

>COVID- Cold weather and snow cannot kill COVID-19<br>
>19 cannot be transmitted through mosquito bites<br>
>Hand dryers are not effective in killing COVID-19<br>
>Ultra-violet disinfection lamp cannot kill the new coronavirus<br>
>Spraying alcohol & chlorine all over your body cannot kill COVID-19<br>
>No other vaccine (like Pneumococcal vaccine) is effective against new coronavirus<br>
>No antibiotics are effective against this virus<br>
These are some facts which people should know about new coronavirus for not getting misguided by anyone. So what about the treatments? Treatment is just being supportive, which means, if they are dry, give them some fluids. if they can’t breathe, put them on the ventilator. But no medication’s going to speed up the effect of the virus to get out of your system more quickly.<br>

The world is going through a very massive health crisis, which demands our utmost care and attention. It is important to follow the guidelines of WHO (world health organization) and governing bodies. And most importantly, don’t panic with the fact that the government is shutting down all the institutes, universities, companies, limiting travel, cinemas, and malls. It does not mean that all population is going to be vanished due to COVID-19 outbreak. These are the safety measure which the government is to prevent the spreading of the virus. So stay calm, wash your hands, don’t panic. We will get rid of this virus soon.<br>

